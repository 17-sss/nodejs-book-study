// * 매개변수와 전달인자는 엄연히 다른 뜻

// * 예제
const Test = (a, b) => {
    return (a + b);
};

let A = 1; 
let B = 2;

console.log('Test(3,4) ->', Test(3,4), ':: 값을 넣어줬으므로 전달인자');
console.log('Test(A,B) ->', Test(A,B), ':: 변수인 값을 넣어줬으므로 매개변수');



/* 
    ​argument 는 전달인자, 변수가 아닌 값을 넣어주는 경우
    parameter 는 매개변수, 변수인 값을 넣어주는 경우를 의미한다.

    line 11 에서 보면 변수가 아닌 3과 4를 넣어 함수를 호출한다. 이때 넣어주는 값을 전달인자라 한다.
    line 12 에서 보면 변수인 A, B를 넣어 함수를 호출한다. 이때 넣어주는 것을 매개변수라 한다.
*/